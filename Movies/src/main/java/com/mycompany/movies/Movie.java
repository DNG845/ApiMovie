import java.util.List;

public class Movie {
    private String title;
    private int year;
    private List<Cast> cast;

    public Movie(String title, int year, List<Cast> cast) {
        this.title = title;
        this.year = year;
        this.cast = cast;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public List<Cast> getCast() {
        return cast;
    }

    public void setCast(List<Cast> cast) {
        this.cast = cast;
    }

}
